#include <bits/stdc++.h>
using namespace std;

int main(){
    // 첫 번째 명령: 북쪽으로 이동
    cout << "move north\n";
    // 두 번째 명령: 동쪽으로 이동
    cout << "move east\n";
    // 세 번째 명령: 남쪽으로 이동
    cout << "move south\n";
    // 네 번째 명령: 서쪽으로 이동
    cout << "move west\n";
    // 다섯 번째 명령: 동쪽으로 이동
    cout << "move east\n";
    // 여섯 번째 명령: 동쪽으로 다시 이동
    cout << "move east\n";
    // 일곱 번째 명령: 남쪽으로 이동
    cout << "move south\n";
    // 여덟 번째 명령: "Ready!" 선언
    cout << "echo Ready!\n";
    // 아홉 번째 명령: "Steady..." 선언
    cout << "echo Steady...\n";
    // 열 번째 명령: 북쪽으로 이동 (특별한 칸으로 이동하여 'win'을 얻기 위함)
    cout << "move north\n";
}
