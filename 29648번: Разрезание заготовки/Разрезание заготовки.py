def find_side_length(a, b, S):
    # Вычисляем максимально возможную длину стороны заготовки
    max_L = int((S + a * b) ** 0.5)
    
    # Проверяем каждую возможную длину стороны от 1 до max_L
    for L in range(1, max_L + 1):
        # Проверяем, может ли площадь S быть получена при данной длине стороны L
        if S == (L * L) - (a * L) - (b * L) + (a * b):
            return L
    
    # Если не найдено подходящей длины стороны, возвращаем -1
    return -1

# Чтение входных данных
a, b, S = map(int, input().split())

# Вызов функции и вывод результата
print(find_side_length(a, b, S))